export interface LeadDetailPanelProps {
  lead: Record<string, any> | null;
  open: boolean;
  onClose: () => void;
}

interface Interaction {
  id: number;
  lead_id?: number;
  Leads_id?: number;
  direction: string;
  /** DB column is uppercase "Content" — API returns this form */
  Content?: string;
  /** fallback lowercase form */
  content?: string;
  created_at?: string;
  type?: string;
  /** DB column is uppercase "Who" — AI agent name or sender identifier */
  Who?: string;
  who?: string;
  /** true when the message was generated by AI */
  ai_generated?: boolean;
  /** true when a human agent manually sent this message */
  is_manual_follow_up?: boolean;
  is_bump?: boolean;
  status?: string;
  sentiment_detected?: string;
}

interface TagData {
  id: number;
  name: string;
  color?: string | null;
  category?: string | null;
  slug?: string | null;
}

interface LeadTagEntry {
  id?: number;
  tagsId?: number;
  Tags_id?: number;
  leadsId?: number;
  Leads_id?: number;
  tagName?: string | null;
}

// ── Helpers ──────────────────────────────────────────────────────────────────

function fmtDate(s: string | null | undefined): string {
  if (!s) return "—";
  try {
    return new Date(s).toLocaleDateString(undefined, {
      day: "2-digit",
      month: "short",
      year: "numeric",
    });
  } catch {
    return s;
  }
}

function fmtDateTime(s: string | null | undefined): string {
  if (!s) return "—";
  try {
    return new Date(s).toLocaleString(undefined, {
      day: "2-digit",
      month: "short",
      year: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    });
  } catch {
    return s;
  }
}

